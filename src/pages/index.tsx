import type { NextPage } from "next";
import Head from "next/head";
import { CalendarForm } from "../components/CalendarForm";
import { useRouter } from "next/router";
import { LangContext } from "~/locales/allLocales";
import { useContext } from "react";

const Home: NextPage = () => {
  const { locale } = useRouter();
  const locales = useContext(LangContext);
  let currentLocale = locales.en;
  if (locale === "es") {
    currentLocale = locales.es;
  }
  return (
    <>
      <Head>
        <title>{currentLocale.title}</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/calculator-svgrepo-com.svg" />
      </Head>
      <main className="flex  min-h-screen flex-col  items-center  bg-template_off_white font-['Poppins']">
        <CalendarForm />
      </main>
    </>
  );
};
export default Home;
